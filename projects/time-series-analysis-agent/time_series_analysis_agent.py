def time_series_agent(
    question: str,
    df: pd.DataFrame,
    model_name: str = "gemini-2.0-flash-exp",
    temperature: float = 0.2,
    show_code: bool = True,
    show_logs: bool = True,
) -> Dict[str, Any]:
    """
    End-to-end time series analysis agent.

    Args:
        question: Natural language query
        df: Pandas DataFrame with transaction data
        model_name: Gemini model to use
        temperature: LLM temperature (0.0-1.0)
        show_code: Display generated code
        show_logs: Display execution logs

    Returns:
        Dictionary with generated code, answer, and metadata
    """
    # Display question
    print_html(question, title="üìã User Question")

    # Generate code
    print("\nü§ñ Generating analysis plan...")
    try:
        full_content = generate_llm_code(
            question=question,
            df=df,
            model_name=model_name,
            temperature=temperature,
        )
    except Exception as e:
        print(f"‚ùå Error during code generation: {e}")
        return {
            "question": question,
            "full_response": "",
            "execution": {
                "code": "",
                "stdout": "",
                "error": str(e),
                "answer_text": f"Failed to generate code: {e}",
                "answer_json": None,
                "answer_df": None,
                "status": "error",
            }
        }

    # Check if response is empty
    if not full_content or not full_content.strip():
        print("‚ö†Ô∏è Warning: LLM returned empty response")
        return {
            "question": question,
            "full_response": "",
            "execution": {
                "code": "",
                "stdout": "",
                "error": "Empty LLM response",
                "answer_text": "The LLM returned an empty response. Please try again.",
                "answer_json": None,
                "answer_df": None,
                "status": "error",
            }
        }

    # Display full response (optional)
    if show_code:
        print_html(full_content, title="üíª Generated Plan & Code")

    # Execute code
    print("\n‚öôÔ∏è Executing analysis...")
    exec_result = execute_generated_code(
        code_or_content=full_content,
        df=df,
        user_request=question,
    )

    # Display logs
    if show_logs and exec_result["stdout"]:
        print_html(exec_result["stdout"], title="üìä Execution Logs")

    # Display errors if any
    if exec_result["error"]:
        print_html(exec_result["error"], title="‚ùå Execution Error")

    # Display answer
    status_emoji = {
        "success": "‚úÖ",
        "no_data": "‚ö†Ô∏è",
        "invalid_date": "‚ö†Ô∏è",
        "invalid_filter": "‚ö†Ô∏è",
        "ambiguous_request": "‚ùì",
        "error": "‚ùå",
    }.get(exec_result["status"], "‚ÑπÔ∏è")

    if exec_result["answer_text"]:
        print_html(
            exec_result["answer_text"],
            title=f"{status_emoji} Analysis Result (Status: {exec_result['status']})"
        )

    # Display structured data if available
    if exec_result["answer_df"] is not None:
        print("\nüìà Detailed Results:")
