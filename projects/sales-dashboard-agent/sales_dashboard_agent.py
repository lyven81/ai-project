def sales_dashboard_agent(
    question: str,
    *,
    db,
    orders_tbl,
    model: str = "gemini-2.0-flash-exp",
    temperature: float = 0.3,
    verbose: bool = True,
) -> dict:
    """
    End-to-end sales dashboard agent:
      1) Generate plan-as-code from question
      2) Execute in controlled namespace
      3) Return results with full transparency

    Returns:
      {
        "question": user question,
        "full_content": raw LLM response,
        "exec": {code, stdout, error, answer}
      }
    """
    if verbose:
        print_html(question, "üí¨ User Question")

    # Generate plan-as-code
    full_content = generate_llm_code(
        question,
        orders_tbl=orders_tbl,
        model=model,
        temperature=temperature,
    )

    if verbose:
        print_html(full_content, "ü§ñ Generated Plan (with Code)")

    # Execute the plan
    exec_res = execute_generated_code(
        full_content,
        db=db,
        orders_tbl=orders_tbl,
        user_request=question,
    )

    # Show results
    if verbose:
        if exec_res["error"]:
            print_html(exec_res["error"], "‚ùå Execution Error")
        else:
            print_html(exec_res["answer"], "‚úÖ Answer")
            if exec_res["stdout"]:
                print_html(exec_res["stdout"], "üìã Debug Logs")

    return {
        "question": question,
        "full_content": full_content,
        "exec": exec_res,
    }

